-- Trigger para creación automática de perfil
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.usuarios (id, email, full_name, role)
  values (new.id, new.email, new.raw_user_meta_data->>'full_name', 'cajero')
  on conflict (id) do nothing;
  return new;
end;
$$ language plpgsql security definer;

-- Recrear el trigger para evitar errores de duplicado
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

